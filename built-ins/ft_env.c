#include "../include/minishell.h"

void	ft_env(char *ms_env[], int fd)
{
	int	i;

	i = 0;
	while (ms_env[i] != NULL)
	{
		ft_dprintf(fd, "%s\n", ms_env[i]);
		i++;
	}
}